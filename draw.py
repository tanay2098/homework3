import turtle#importing turtle package
t=turtle.Turtle()# creating an instance of turtle class

t.color('red')#setting the pen color to red
t.setheading(135)#setting the turtle heading to 135 degrees
t.forward(70)#Move the turtle forward by 70
t.setheading(45)#setting the turtle heading to 45 degrees
t.forward(110)#Move the turtle forward 110
t.setheading(315)#Setting the turtle heading to 315 degrees
t.forward(70)#Moving forward the turtle by 70
t.color('blue')#Changing the pen color to blue
t.setheading(45)#Setting turtle heading to 45 degrees
t.forward(70)#Moving turtle forward by 70
t.setheading(315)#Setting the turtle heading to 315
t.forward(110)#Moving the turtle forward by 110
t.setheading(225)#Setting the turtle heading to 225
t.forward(70)#moving turtle forward by 70

turtle.done()#To make the graphics stay in non-interactive execution